let color = 'green';

switch (color) {
    case 'red':
        console.log('the color is red');
        break;
    case 'blue':
        console.log('the color is blue');
        break;
    case 'green':
        console.log('the color is green');
        break
    case 'white':
        console.log('the color is white');
        break
    default:
        console.log('color is unknown');
}